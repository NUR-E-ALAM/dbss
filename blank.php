তবে শর্ত থাকে যে, কোন সমিতিতে সরকারের এক তৃতীয়াংশ শেয়ার থাকিলে বা উক্ত সমিতি সরকারের নিকট হইতে ঋণ গ্রহণ করিয়া থাকিলে বা সমিতি কর্তৃক গৃহীত ঋণ পরিশোধের গ্যারান্টি সরকার প্রদান করিয়া থাকিলে নিবন্ধক বিশেষ সভা আহ্বানের পরিবর্তে উক্ত সমিতির ব্যবস্থাপনা কমিটির দায়ী সদস্যগণকে কারণ দর্শানোর সুযোগ দিয়া তাহাদেরকে কমিটি হইতে বহিষ্কার করিতে বা প্রয়োজনবোধে সম্পূর্ণ কমিটি ভাংগিয়া দিতে পারিবেন৷
<br>
(২) উপ-ধারা (১) এর অধীনে নির্দেশ দেওয়া সত্ত্বেও ব্যবস্থাপনা কমিটি বিশেষ সাধারণ সভা আহ্বান না করিলে নিবন্ধক কারণ দর্শানোর যুক্তি সংগত সুযোগ দিয়া দোষী সদস্য বা সদস্যগণকে বহিষ্কার করিতে বা প্রয়োজনবোধে সম্পূর্ণ কমিটিকে ভাংগিয়া দিতে পারিবেন৷
<br>
(৩) যে সমিতিতে সরকারের এক তৃতীয়াংশ শেয়ার আছে বা যে সমিতি সরকারের নিকট হইতে ঋণ গ্রহণ করিয়াছে বা যে সমিতি কর্তৃক গৃহীত ঋণ পরিশোধের গ্যারাণ্টি সরকার প্রদান করিয়াছে, সেই সমিতির বিষয়াবলী সরকার যে কোন সময় তদন্ত করিতে পারিবে এবং এইরূপ তদন্তে যদি দেখা যায় যে, সমিতির কাজ কর্ম এই আইন, বিধি বা উপ-আইন লংঘন করিয়া পরিচালিত হইয়াছে বা হইতেছে এবং উক্ত লংঘন সরকার প্রদত্ত ঋণ বা গ্যারান্টি বা সাধারণ সদস্যদের স্বার্থের জন্য ক্ষতিকর তাহা হইলে সরকার কারণ দর্শানোর যুক্তি সংগত সুযোগ দিয়া সরকারের বিবেচনায় উক্ত লংঘনের জন্য দায়ী সদস্য বা সদস্যগণকে ব্যবস্থাপনা কমিটি হইতে বহিষ্কার করিতে বা সম্পূর্ণ ব্যবস্থাপনা কমিটি ভাংগিয়া দিতে পারিবে৷
<br>
(৪) এই ধারার অধীনে আহ্ববানকৃত বিশেষ সাধারণ সভার সিদ্ধান্ত অনুসারে বা উপ-ধারা (১) এর শর্তাংশ অনুযায়ী উপ-ধারা (২) বা (৩) অনুসারে ব্যবস্থাপনা কমিটির কোন সদস্যকে বহিষ্কার করা হইলে বা উক্ত কমিটি ভাংগিয়া দেওয়া হইলে বহিষ্কৃত সদস্য বা ভাংগিয়া দেওয়া কমিটির সকল ৩৭[ সদস্যকে নিবন্ধক পরবর্তী তিন বত্সরের জন্য ব্যবস্থাপনা কমিটির সদস্য হিসাবে নির্বাচিত হইবার অযোগ্য ঘোষণা করিতে পারিবেন৷]৷
<br>
(৫) এই ধারার অধীনে নিবন্ধক ব্যবস্থাপনা কমিটির কোন সদস্যকে বহিষ্কার করিলে বা ব্যবস্থাপনা কমিটি ভাংগিয়া দিলে উক্ত সিদ্ধান্ত দ্বারা সংক্ষুব্ধ ব্যক্তি উক্ত সিদ্ধান্ত প্রদানের ৩০ (ত্রিশ) দিনের মধ্যে বিধি দ্বারা নির্ধারিত পদ্ধতিতে সিদ্ধান্ত প্রদানকারী নিবন্ধকের পরবর্তী ঊর্ধ্বতন কর্তৃপক্ষের বরাবরে আপীল করিতে পারিবে এবং এইরূপ সিদ্ধান্ত ৩৮[ সরকার প্রদান করিয়া থাকিলে সংক্ষুব্ধ ব্যক্তি সিদ্ধান্তটি পুনঃবিবেচনার জন্য উক্ত সময়সীমার মধ্যে] সরকারের নিকট আবেদন করিতে পারিবে৷
<br>
(৬) উপ-ধারা (৫) এর অধীনে পেশকৃত আপীল বা পুনঃবিবেচনা আবেদনের উপরে প্রদত্ত সিদ্ধান্ত চূড়ান্ত হইবে এবং তদ্‌সম্পর্কে ৩৯[ ধারা ৫২] এর অধীনে জেলা জজের নিকট বা অন্য কোন আদালতে কোন প্রশ্ন উত্থাপন করা যাইবে না৷
<br>
(৭) এই ধারার অধীনে কোন সমবায় সমিতির ব্যবস্থাপনা কমিটি ভাংগিয়া দেওয়া হইলে নিবন্ধক সমিতির ব্যবস্থাপনা নির্বাহের জন্য ও নির্বাচন অনুষ্ঠানের জন্য ৪০[ ধারা ১৯ এর বিধান অনুযায়ী যোগ্য কোন সদস্য বা সরকারি কর্মকর্তাদের সমন্বয়ে ১২০ (একশত বিশ) দিনের জন্য] একটি অন্তর্বর্তী ব্যবস্থাপনা কমিটি নিয়োগ করিবেন৷
<br>
৪১[ (৮) উপ-ধারা (৭) এর অধীন গঠিত অন্তর্বর্তী ব্যবস্থাপনা কমিটি দায়িত্ব গ্রহণের ১২০ (একশত বিশ) দিনের মধ্যে নির্ধারিত পদ্ধতিতে ব্যবস্থাপনা কমিটির নির্বাচন অনুষ্ঠানের ব্যবস্থা করিবে এবং নির্বাচিত কমিটির নিকট অবিলম্বে দায়িত্ব হস্তান্তর করিবে। ]
<br>
(৯) অন্তর্বর্তী ব্যবস্থাপনা কমিটি উপ-ধারা (৮) অনুসারে যথাসময়ে কার্যক্রম গ্রহণে ব্যর্থ হইলে নিবন্ধক উক্ত কমিটি বিলুপ্ত করিয়া নতুন ৪২[ ১২০ (একশত বিশ) দিনের জন্য] অন্তর্বর্তী ব্যবস্থাপনা কমিটি গঠন করিবেন৷
 	 
 	 <h3>সমবায় সমিতির ঠিকানা	</h3>
 	
<hr> 	
৪৩[ ২৩৷ উপ-আইনে পূর্ণ ঠিকানা উল্লেখসহ প্রত্যেক সমবায় সমিতির একটি কার্যালয় থাকিবে এবং উক্ত ঠিকানায় সকল নোটিশ প্রেরণসহ সব ধরনের যোগাযোগ রক্ষা করা হইবে।]
<br> 
 	 
 	
সমবায় সমিতির শাখা অফিস খোলা এবং উহার নামের সহিত ব্যাংক শব্দ ব্যবহারের উপর বাধা নিষেধ	 	
৪৪[ ২৩ক। (১) কোন সমবায় সমিতি উহার কার্যক্রম পরিচালনার জন্য কোন শাখা অফিস খুলিতে পারিবে না, তবে এই বিধান কার্যকর হইবার পূর্বে কোন অনুমোদিত শাখা অফিস থাকিলে, উহা এই বিধান কার্যকর হইবার ৬ (ছয়) মাসের মধ্যে স্বয়ংক্রিয়ভাবে মূল সমিতির সাথে একীভূত হইবে অথবা সংশ্লিষ্ট সমবায় সমিতির আবেদনক্রমে উক্ত শাখা অফিস প্রাথমকি সমবায় সমিতি হিসাবে নিবন্ধিত হইতে পারিবে। 
<br>
(২) এই আইনের অধীন নিবন্ধিত সমবায় ভূমি উন্নয়ন ব্যাংক, কেন্দ্রীয় সমবায় ভূমি উন্নয়ন ব্যাংক, কেন্দ্রিয় সমবায় ব্যাংক এবং বাংলাদেশ সমবায় ব্যাংক ব্যতীত কোন প্রাথমিক সমবায় সমিতি, কেন্দ্রীয় সমবায় সমিতি বা জাতীয় সমবায় সমিতি উহার নামের সহিত ব্যাংক শব্দ ব্যবহার করিতে পারিবে না, তবে কোন সমবায় সমিতি এইরূপ শব্দযুক্ত নামে নিবন্ধিত হইয়া থাকিলে এই বিধান কার্যকর হইবার ৩ (তিন) মাসের মধ্যে উহার নাম সংশোধন করিয়া নিবন্ধককে অবহিত করিতে হইবে।
<br>
(৩) কোন ব্যক্তি এই ধারার কোন বিধান লঙ্ঘন করিলে অনধিক ৭ (সাত) বৎসর কারাদণ্ড বা অন্যূন ১০ (দশ) লক্ষ টাকা অর্থদণ্ড বা উভয়দণ্ডে দণ্ডনীয় হইবেন।
 	 
<br> 
 	
বাংলাদেশ ব্যাংকের অনুমোদন ব্যতীত সমবায় সমিতি কর্তৃক ব্যাংকিং ব্যবসা পরিচালনার উপর বাধা নিষেধ	 	
২৩খ। - (১) কোন সমবায় সমিতি বাংলাদেশ ব্যাংকের পূর্বানুমোদন ব্যতিরেকে ব্যাংকিং ব্যবসা পরিচালনা করিতে পারিবে না। 
<br>
(২) কোন ব্যক্তি এই ধারার কোন বিধান লঙ্ঘন করিলে অনধিক ৭ (সাত) বৎসর কারাদণ্ড বা অন্যূন ১০ (দশ) লক্ষ টাকা অর্থদণ্ড বা উভয়দণ্ডে দণ্ডনীয় হইবেন।]
 	 
 	 
