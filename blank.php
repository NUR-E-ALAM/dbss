<h3>সদস্যদের ভোট</h3>	 	
৩৬৷ (১) সকল শ্রেণীর সমবায় সমিতির প্রত্যেক সদস্য সমিতির কর্মকাণ্ডের সিদ্ধান্ত গ্রহণের ক্ষেত্রে একটি মাত্র ভোট প্রয়োগের অধিকারী হইবেন; উক্ত ভোট ব্যক্তিগতভাবে উপস্থিত হইয়া প্রয়োগ করিতে হইবে, প্রক্সির মাধ্যমে কোন ভোট দেওয়া যাইবে না৷
<br>
(২) ভোটে সমতা দেখা দিলে সভাপতির একটি দ্বিতীয় বা নির্ণায়ক ভোট প্রদানের ক্ষমতা থাকিবে৷
<br>
(৩) প্রাথমিক সমবায় সমিতি ব্যতীত অন্যান্য সমবায় সমিতির ক্ষেত্রে, একটি সদস্য সমিতি উহার বৈধ কোন সদস্যকে সদস্য-সমিতির প্রতিনিধিত্বকারী ব্যক্তিকে উহার প্রতিনিধি হিসাবে ভোটদানের জন্য মনোনয়ন দিতে পারিবে৷
<br>
(৪) সদস্য সমিতির কোন ব্যক্তি ঊর্ধ্বতন সমিতির পক্ষে বা কর্মকাণ্ডে প্রতিনিধিত্ব করিবেন বা কিভাবে ভোট দিবেন সেই সম্পর্কে উপ-আইনে বিস্তারিত বিধান থাকিবে৷
 	 
