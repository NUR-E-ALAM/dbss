তবে শর্ত থাকে যে, কোন সমিতিকে সরকারী ঋণ, বিনিয়োগ, অগ্রিম অথবা অন্যান্য সুবিধা প্রদান করা হইলে বা সরকারী গ্যারান্টি থাকিলে ঐ সকল সমিতি কর্তৃক উল্লিখিত বিক্রয়, বিনিময় বা ইজারা প্রদানের ক্ষেত্রে নিবন্ধকের লিখিত পূর্ব অনুমতি গ্রহণ করিতে হইবে৷
