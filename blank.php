(৪) উপ-ধারা (২) এর অধীনে কোন আবেদন নামঞ্জুর করা হইলে আবেদনকারী নামঞ্জুর হওয়া সংক্রান্ত লিখিত স্মারক জারী করার তারিখ হইতে ৩০ (ত্রিশ) দিনের মধ্যে সিদ্ধান্ত প্রদানকারী কর্তৃপক্ষের পরবর্তী ঊর্ধ্বতন কর্তৃপক্ষের নিকট আপীল করিতে পারিবেন, এবং নামঞ্জুরের সিদ্ধান্তটি নিবন্ধক স্বয়ং প্রদান করিয়া থাকিলে উক্ত সময়সীমার মধ্যে তাহার নিকট আবেদনটি পুনঃবিবেচনার জন্য পেশ করিতে হইবে৷
<br>
(৫) উপ-ধারা (৪) এর অধীনে পেশকৃত আপীল বা পুনঃবিবেচনার আবেদন ৩০ (ত্রিশ) দিনের মধ্যে নিষ্পত্তি করিয়া সংশ্লিষ্ট সিদ্ধান্ত আবেদনকারী এবং নিবন্ধনকারী কর্তৃপক্ষের নিকট প্রেরণ করিতে হইবে এবং এই সিদ্ধান্তই চূড়ান্ত হইবে৷
 	 
 	 
 	<h3>নিবন্ধন সনদ	 </h3>
	<hr>
১১৷ ধারা ১০ এর অধীনে পেশকৃত নিবন্ধনের কোন আবেদন, আপীল বা পুনঃবিবেচনার আবেদন মঞ্জুর করা হইলে নিবন্ধক আবেদনকারীর বরাবরে নির্ধারিত ফরমে একটি নিবন্ধন সনদ ইস্যু করিবেন এবং এই সনদ উক্ত সমিতির নিবন্ধনের ব্যাপারে চূড়ান্ত প্রামাণ্য দলিল হিসাবে গণ্য হইবে৷
 	 
 	 <h3>নিবন্ধরে শর্তাবলী</h3>
 	
<hr>	 	
১২৷ (১) কোন সমবায় সমিতি নিবন্ধনের উদ্দেশ্যে পেশকৃত আবেদনের সহিত সংযুক্ত উ-আইনের খসড়া, এই আইন ও বিধির প্রতি লক্ষ্য রাখিয়া নিবন্ধক সংশোধনের জন্য আবেদনকারীকে প্রয়োজনীয় নির্দেশ দিতে পারিবেন৷

<br>

(২) ধারা ১০ এর অধীনে নিবন্ধনের আবেদন মঞ্জুর করা হইলে নিবন্ধক সমবায় সমিতির বরাবরে নিবন্ধন সনদ ইস্যু করার সময় দাখিলকৃত বা, ক্ষেত্রমত, সংশোধিত উপ-আইনের তিনটি কপির প্রতি পৃষ্ঠা তাহার স্বাক্ষর ও সীল যুক্ত করিয়া দুইটি কপি আবেদনকারীকে ফেরত দিবেন এবং একটি তাহার কার্যালয়ে সংরক্ষণ করিবেন৷
<br>
(৩) কোন শ্রেণীর সমবায় সমিতির নিবন্ধনের ব্যাপারে সরকার প্রয়োজন মনে করিলে বিধি দ্বারা নির্ধারিত শর্তাবলী আরোপ করিতে পারিবে৷
 	 
 	 
 	<h3>উপ-আইন সংশোধন, ইত্যাদি	 </h3>
<hr>	
১৩৷ (১) নিবন্ধিত সমবায় সমিতি বিধি দ্বারা নির্ধারিত পদ্ধতিতে উহার অনুমোদিত উপ-আইন সংশোধন বা সম্পূর্ণরূপে বাতিল করিয়া নূতন ভাবে প্রণয়ন করিতে পারিবে এবং এইরূপ সংশোধন বা পুনঃপ্রণীত উপ-আইনের খসড়া প্রাপ্তির তারিখ হইতে ষাট দিনের মধ্যে নিবন্ধক অনুমোদন করিবেন:
<br>
২০[ তবে শর্ত থাকে যে, উক্ত সময় সীমার মধ্যে প্রস্তাবিত সংশোধন বা পুনঃপ্রণীত উপ-আইন অনুমোদন করা না হইলে উহার কারণ উল্লেখ করিয়া ৬০ (ষাট) কার্য দিবসের মধ্যে নিবন্ধক আবেদনকারী সমিতিকে লিখিতভাবে জানাইয়া দিবেন।]
<br>
২১[ (১ক) যদি কোন সমবায় সমিতির উপ-আইন বা উহার অংশ বিশেষ এই আইনের সহিত অসঙ্গতিপূর্ণ হয় বা অর্থ সরবরাহকারী সংস্থা উহার সদস্য সমিতিকে উহার উপ-আইন সংশোধনের জন্য নির্দেশনা প্রদান করে সংশ্লিষ্ট সমবায় সমিতি ১২০ (একশত বিশ) দিনের মধ্যে নির্ধারিত পদ্ধতিতে উপ-আইন সংশোধন করিতে বাধ্য থাকিবে এবং এই ক্ষেত্রে উপ-আইন সংশোধনের জন্য সাধারণ সভার অনুমোদনের প্রয়োজন হইবে না।
<br>
(১খ) উপ-ধারা (১ক) এ উল্লিখিত সময়ের মধ্যে সংশ্লিষ্ট সমবায় সমিতি উহার উপ-আইন সংশোধনে ব্যর্থ হইলে, নিবন্ধক উক্ত সময়সীমা অতিবাহিত হইবার পর উক্ত সমিতির উপ-আইন সংশোধন করিয়া সমিতিকে অবহিত করিবেন।]
<br>
(২) প্রত্যেক সমবায় সমিতি উহার উপ-আইন, হালনাগাদ সংশোধনীসহ, যদি থাকে, মুদ্রণ করিয়া সকল সদস্যের নিকট, সমিতি কর্তৃক নির্ধারিত মূল্যে, বিতরণের ব্যবস্থা করিবে৷
 	 
 	 
<h2>চতুর্থ অধ্যায় সমবায় সমিতির আইনগত মযার্দা, ব্যবস্থাপনা ইত্যাদি</h2>
<hr>
 	<h3>প্রত্যেক সমবায় সমিতি একটি সংবিধিবদ্ধ সংস্থা	 </h3>
<hr>	
১৪৷ (১) এই আইনের অধীনে নিবন্ধিত প্রত্যেক সমবায় সমিতি হইবে স্বতন্ত্র আইনগত সত্বাবিশিষ্ট একটি সংবিধিবদ্ধ সংস্থা (body corporate) যাহার স্থায়ী ধারাবাহিকতা থাকিবে, উহার উদ্দেশ্য পূরণকল্পে যে কোন ধরনের সম্পদ অর্জন, ধারণ, হস্তান্তর করার এবং চুক্তি করার অধিকার থাকিবে; সমিতির একটি সাধারণ সীলমোহর থাকিবে এবং সমিতি উহার নিজ নামে মামলা দায়ের করিতে পারিবে এবং উক্ত নামে উহার বিরুদ্ধেও মামলা দায়ের করা যাইবে৷
<br>
(২) নিবন্ধিত সমবায় সমিতির সাধারণ সীলমোহর কাহার তত্ত্বাবধানে থাকিবে, কোন্‌ কোন্‌ দলিলে ও কোন্‌ কর্তৃপক্ষের উপস্থিতিতে সীলমোহর দ্বারা সীল দিতে হইবে তাহা উপ-আইন দ্বারা নির্ধারিত হইবে৷
 	 
 	 <h3>সমবায় সমিতির শেয়ার মূলধন ও শেয়ার সম্পর্কে সদস্যগণের দায়-দায়িত্ব	 	</h3>
 	
<hr>
১৫৷ (১) এই আইনের অধীনে নিবন্ধনযোগ্য প্রত্যেকটি সমবায় সমিতির শেয়ার মূলধন থাকিবে যাহা সংশ্লিষ্ট সমবায় সমিতির উপ-আইনে নির্ধারিত মূল্যমানের এবং নির্ধারিত সংখ্যক শেয়ারে বিভক্ত থাকিবে৷
<br>
২২[ (২) কোন সমবায় সমিতির নিবন্ধনকালে উহার প্রত্যেক সদস্যকে অন্ততঃ একটি শেয়ার অভিহিত মূল্যে (face value) ক্রয় করিতে হইবে এবং পরবর্তী সময়ে নতুন সদস্যপদ লাভের জন্য বা কোন সদস্য কর্তৃক অতিরিক্ত শেয়ার ক্রয়ের ক্ষেত্রে ব্যবস্থাপনা কমিটি কর্তৃক অথবা, ক্ষেত্রমত, নিবন্ধক কর্তৃক নির্ধারিত শেয়ারের বাজার মূল্য (market value) সমিতিকে প্রদান করিতে হইবে, যাহা সমিতির নিজস্ব মূলধন হিসেবে পরিগণিত হইবেঃ
<br>
তবে শর্ত থাকে যে, সরকার ব্যতীত, কোন সদস্য বা, ক্ষেত্রমত, সমিতি কোন সমবায় সমিতির মোট শেয়ার মূলধনের এক পঞ্চমাংশের অধিক শেয়ার ক্রয় করিতে পারিবে না।]
<br>
(৩) কোন সমবায় সমিতির শেয়ার সমিতির নিকট ফেরত্যোগ্য হইবে না বা সমিতি উক্ত শেয়ার ক্রয় বা উহার পরিবর্তে অর্থ বা অন্য কোন সম্পদ উক্ত সদস্যকে পরিশোধ করিতে পারিবে না:
<br>
তবে শর্ত থাকে যে, কোন সমবায় সমিতির সদস্য পদ যদি উহার উপ-আইন অনুসারে কোন সরকারী বা স্থানীয় কর্তৃপক্ষ বা কোন শিল্প বা ব্যবসা প্রতিষ্ঠানের বেতনভোগী কর্মচারী বা শ্রমিকদের মধ্যে সীমিত রাখা বাধ্যতামূলক হয়, তাহা হইলে উক্ত সদস্যগণের ধারণকৃত শেয়ারের ক্ষেত্রে এই উপ-ধারায় বর্ণিত নিষেধাজ্ঞা প্রযোজ্য হইবে না৷
<br>
(৪) কোন সদস্য তাহার শেয়ার সমবায় সমিতির ব্যবস্থাপনা কমিটির পূর্বসম্মতিক্রমে উপ-আইন অনুসারে হস্তান্তর করিতে পারিবে৷
<br>
(৫) সমবায় সমিতির অবসায়নের সময় উহার দায়-দায়িত্ব পরিশোধের ক্ষেত্রে সমিতির পরিসম্পদে ঘাটতি থাকিলে উহা পরিশোধের জন্য সদস্যগণ তাহাদের নিজ নিজ শেয়ারের অনুপাতে দায়ী থাকিবেন৷
 	 
 	 
 	<h3>সমবায় সমিতির চূড়ান্ত কর্তৃপক্ষ</h3>
<hr>	 	
১৬৷ (১) এই আইন, বিধি এবং উপ-আইনের শর্ত সাপেক্ষে প্রত্যেক সমবায় সমিতির চূড়ান্ত কর্তৃত্ব উহার সাধারণ সভার উপর বর্তাইবে৷
<br>
(২) সাধারণ সভা আহ্বান এবং ইহার কার্যক্রম পরিচালনার ক্ষেত্রে এই আইন, বিধি ও উপ-আইন অনুসরণ করিতে হইবে৷
 	 
 	 <h3>বার্ষিক সাধারণ সভা ও বিশেষ সাধারণ সভা</h3>
 	
<hr>	 	
১৭৷ (১) সমবায় সমিতি উহার সদস্যগণের সমন্বয়ে দুই প্রকার সভা অনুষ্ঠান করিতে পারে, যথাঃ বার্ষিক সাধারণ সভা ও বিশেষ সাধারণ সভা৷
<br>
(২) ব্যবস্থাপনা কমিটি সমবায় সমিতির বার্ষিক সাধারণ সভা বত্সরে একবার অনুষ্ঠানের ব্যবস্থা করিবে; এবং অন্য যে কোন সাধারণ সভা বিশেষ সাধারণ সভা নামে অভিহিত হইবে; উভয় প্রকারের সাধারণ সভা এই আইন ও বিধি অনুসারে অনুষ্ঠিত হইবে, তবে সমিতির নিজস্ব বৈশিষ্ট্য অনুসারে উপ-আইনেও এই ব্যাপারে অতিরিক্ত বিধান থাকিতে পারে৷
<br>
২৩[ (৩) প্রত্যেক সমবায় সমিতির বার্ষিক সাধারণ সভা উহার নিরীক্ষা কাজ সম্পন্ন হইবার ৬০ (ষাট) দিনের মধ্যে নির্ধারিত পদ্ধতিতে অনুষ্ঠিত হইবেঃ
<br>
তবে শর্ত থাকে যে, সমিতির আবেদনের প্রেক্ষিতে নিবন্ধক যদি এই মর্মে সন্তুষ্ট হন যে, উক্তরূপ সময়ের মধ্যে বার্ষিক সাধারণ সভা অনুষ্ঠিত না হইবার যুক্তিসঙ্গত কারণ রহিয়াছে, তাহা হইলে নিবন্ধক উক্ত সময়সীমা সর্বোচ্চ ৬০ (ষাট) দিন বৃদ্ধি করিতে পারিবেন।]

<h4>(৪) সাধারণ সভার কার্যাবলী হইবে নিম্নরূপ:-</h4>
<hr>
(ক) বিগত বার্ষিক সাধারণ সভাসহ বিশেষ সাধারণ সভার কার্য বিবরণী অনুমোদন;
<br>
(খ) ব্যবস্থাপনা কমিটির কার্যক্রমের উপর বাত্সরিক রিপোর্ট পর্যালোচনা;
<br>
(গ) বার্ষিক হিসাব বিবরণী বিবেচনা ও অনুমোদন;
<br>
(ঘ) উদ্বৃত্তপত্র ও নিরীক্ষা প্রতিবেদন পর্যালোচনা:
<br>
তবে শর্ত থাকে যে, নিরীক্ষিত উদ্বৃত্তপত্রের এক কপি সাধারণ সভার নোটিশের সাথে প্রত্যেক সদস্যের নিকট প্রেরণ করিতে হইবে;
<br>
(ঙ) পরবর্তী আর্থিক বত্সরের জন্য প্রাক্কলিত বাজেট পর্যালোচনা ও অনুমোদন;
<br>
(চ) ঋণ গ্রহণের সর্বোচ্চ সীমা নির্ধারণ;
<br>
(ছ) সমবায় সমিতির কোন সদস্য বা কর্মচারী কর্তৃক কোন অভিযোগ বা সদস্যপদ সংক্রান্ত বিষয়ে সাধারণ সভা অনুষ্ঠানের কমপক্ষে ত্রিশ দিন পূর্বে কোন নোটিশ সমিতিতে দাখিল করা হইলে উক্ত বিষয়ে শুনানী, পর্যালোচনা ও সিদ্ধান্ত গ্রহণ;
<br>
(জ) সমবায় সমিতির কর্মচারী নিয়োগ সংক্রান্ত বিষয়, তাহাদের বেতন নির্ধারণ ও সার্ভিস রুল অনুমোদন;
<br>
(ঝ) সমিতির নিরীক্ষা প্রতিবেদন বা তদন্ত রিপোর্টে উল্লিখিত বিষয়ে নিবন্ধকের বরাবরে প্রেরণের জন্য পরিপালন পত্র অনুমোদন;
<br>
(ঞ)	ব্যবস্থাপনা কমিটির নির্বাচন অনুষ্ঠান বা ব্যবস্থাপনা কমিটির সকল বা বিশেষ কোন সদস্যের বহিষ্কার বা সমিতির অন্য কোন সদস্যকে বহিষ্কার;
<br>
(ট) উপ-আইন সংশোধন বা পুনঃপ্রণয়ন৷
<br>
(৫) যে সকল সমবায় সমিতির সদস্য সংখ্যা একশত বা ইহার কম, সেই সকল সমবায় সমিতির সাধারণ সভার কোরাম হইবে মোট সদস্য সংখ্যার এক তৃতীয়াংশ; এবং সদস্য সংখ্যা একশত এর অধিক কিন্তু এক হাজারের কম হইলে কোরামের জন্য সদস্য সংখ্যা হইবে মোট সদস্য সংখ্যার এক চতুর্থাংশ; এবং এক হাজার বা তাহার অধিক সদস্য বিশিষ্ট সমিতির সভার কোরামের জন্য উহার মোট সদস্য সংখ্যার এক পঞ্চমাংশ সদস্যের উপস্থিতি৷
<br>
(৬) আইন ও বিধি মোতাবেক যথাসময়ে সাধারণ সভা অনুষ্ঠানে ব্যর্থ হইলে তজ্জন্য দায়ী ব্যবস্থাপনা কমিটির সংশ্লিষ্ট সদস্য বা সদস্যগণ উক্ত সমবায় সমিতির ব্যবস্থাপনা কমিটির পরবর্তী নির্বাচনে সদস্য হওয়ার ক্ষেত্রে তিন বত্সরের জন্য ২৪[ অযোগ্য হইবেন] ।
<br>
(৭) ধারাবাহিকভাবে পর পর তিন বত্সর যদি কোন সমবায় সমিতির সাধারণ সভায় কোরাম না হয়, তবে ঐ সমিতি অবসায়নের যোগ্য বলিয়া নিবন্ধক আদেশ দিতে এবং প্রয়োজনীয় কার্যক্রম গ্রহণ করিতে পারিবেন, তবে পর পর দুই বত্সর কোরাম অর্জনে ব্যর্থ সমিতিকে নিবন্ধক এই বিষয়ে সতর্ক করিয়া দিবেন৷
<br>
(৮) কোন সমিতির বিশেষ সাধারণ সভা আহ্বান করিতে হইবে, যদি-
<br>
(ক) এই আইনের বিধান অনুসারে উক্ত সভা আহ্বানের প্রয়োজন হয়;
<br>
(খ) ব্যবস্থাপনা কমিটি কোন বিশেষ কারণে উক্ত সভা আহ্বান প্রয়োজনীয় বলিয়া সিদ্ধান্ত গ্রহণ করে;
<br>
(গ) অনধিক পাঁচশত সদস্য বিশিষ্ট সমবায় সমিতির ক্ষেত্রে এক-তৃতীয়াংশ এবং অন্যান্য সমবায় সমিতির ক্ষেত্রে এক পঞ্চমাংশ সদস্য লিখিতভাবে ব্যবস্থাপনা কমিটির নিকট আবেদন করেন;
<br>
(ঘ) এইরূপ সভা আহ্বানের জন্য নিবন্ধকের নির্দেশ থাকে৷
<br>
(৯) নিবন্ধক বা তত্কর্তৃক লিখিত নির্দেশবলে ক্ষমতাপ্রাপ্ত কোন ব্যক্তি কোন সমিতির বিশেষ সাধারণ সভা আহ্বান করিতে পারিবেন যদি ঐ সমিতির ব্যবস্থাপনা কমিটি নিবন্ধকের নির্দেশে বা সদস্যদের লিখিত আবেদনের প্রেক্ষিতে সাধারণ সভা আহ্বান করিতে ব্যর্থ হয়৷
<br>
(১০) সাধারণ সভা বা বিশেষ সভার সিদ্ধান্ত মোতাবেক ব্যবস্থাপনা কমিটির যে কোন বা সকল নির্বাচিত সদস্যকে বহিষ্কার করা যাইবে যদি এতদুদ্দেশ্যে উক্ত সাধারণ সভা আহ্বান করা হয়; তবে এইরূপ সিদ্ধান্ত অবশ্যই সভায় উপস্থিত দুই তৃতীয়াংশ সদস্য কর্তৃক সমর্থিত হইতে হইবে৷
<br>
(১১) যে সাধারণ সভায় ব্যবস্থাপনা কমিটির কোন সদস্য বহিষ্কৃত হন সেই সভাতেই অপর একজন সদস্যকে তদ্‌স্থলে নির্বাচন করিতে হইবে এবং তিনি বা তাঁহারা উক্ত কমিটির অবশিষ্ট মেয়াদের জন্য নির্বাচিত হইবেন৷
<br>
(১২) উপ-ধারা (৪) এ উল্লিখিত যে কোন বিষয়ে বিশেষ সাধারণ সভায় সিদ্ধান্ত গ্রহণ করা যাইবে৷
 	 
 	 <h3>ব্যবস্থাপনা কমিটি	</h3>
 	
 	<hr>
১৮৷ (১) প্রত্যেক সমবায় সমিতির যাবতীয় ক্ষমতা এবং ব্যবস্থাপনার দায়িত্ব এই আইন, বিধি ও উপ-আইন মোতাবেক গঠিত একটি ব্যবস্থাপনা কমিটির উপর ন্যস্ত থাকিবে এবং সাধারণ সভায় সম্পাদনযোগ্য কার্য ব্যতীত সমিতির সকল কার্য উক্ত কমিটি সম্পাদন করিবে৷
<br>
(২) উপ-আইনে নির্ধারিত সংখ্যক সদস্য সমন্বয়ে ব্যবস্থাপনা কমিটি গঠিত হইবে, এবং তাঁহারা বিধি দ্বারা নির্ধারিত পদ্ধতিতে সাধারণ সভায় নির্বাচিত হইবেন:
<br>
তবে শর্ত থাকে যে,
<br>
(ক) নিবন্ধনের সময় নিবন্ধক তত্কর্তৃক অনুমোদিত উপ-আইন অনুসারে প্রথম ব্যবস্থাপনা কমিটি অনুমোদন করিবেন;
<br>
(খ) যেই সকল সমবায় সমিতির পরিশোধিত শেয়ার মূলধনের ৫০% এর অধিক সরকারের মালিকানায় আছে বা যেই সকল সমবায় সমিতির মোট ঋণের বা অগ্রিমের ৫০% এর অধিক সরকার প্রদান করিয়াছে বা উক্ত সমিতির গৃহীত ঋণের ব্যাপারে সরকারের গ্যারান্টি রহিয়াছে, সেই সকল ক্ষেত্রে ২৫[ নিবন্ধক প্রাথমিক সমবায় সমিতি ও কেন্দ্রীয় সমবায় সমিতির এবং সরকার জাতীয় সমবায় সমিতির] ২৬[ ব্যবস্থাপনা কমিটিতে এক তৃতীয়াংশ সদস্য ব্যবস্থাপনা কমিটির নির্বাচন অনুষ্ঠিত হইবার ৬০ (ষাট) দিনের মধ্যে মনোনয়ন প্রদান করিবেন]
<br>
২৭[ (৩) কোন সমবায় সমিতি নিবন্ধনকালে নিবন্ধক কর্তৃক অনুমোদিত প্রথম ব্যবস্থাপনা কমিটির মেয়াদ হইবে ২(দুই) বৎসর এবং এই মেয়াদের মধ্যে অনুমোদিত ব্যবস্থাপনা কমিটি নির্বাচন অনুষ্ঠান করিয়া নিয়মিত কমিটি গঠন করিবে।]
<br>
২৮[ (৪) নির্বাচিত ব্যবস্থাপনা কমিটি উহার প্রথম অনুষ্ঠিত সভার তারিখ হইতে তিন বত্সর মেয়াদের জন্য দায়িত্ব পালন করিবে এবং উক্ত কমিটি উহার মেয়াদ পূর্তির পূর্বে পরবর্তী ব্যবস্থাপনা কমিটির নির্বাচন সম্পন্ন করিবে৷
<br>
