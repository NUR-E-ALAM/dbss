৩৭৷ কোন সদস্যের নিকট সমিতির চাঁদা বা শেয়ার বা সদস্যপদ সংক্রান্ত অন্য কোন পাওনা বকেয়া থাকিলে উহা পরিশোধ না করা পর্যন্ত উক্ত সদস্য তাঁহার অধিকার প্রয়োগ করিতে পারিবেন না৷
