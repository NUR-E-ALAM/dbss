৩৮৷ অন্য কোন আইনে ভিন্নরূপ যাহা কিছু্‌ই থাকুক না কেন, ধারা ৩১ এর বিধান সাপেক্ষে, সমবায় সমিতির কোন সদস্যের নিকট উক্ত সমিতির প্রাপ্য নহে এমন কোন ঋণ বা দায় পরিশোধের জন্য আদালতের আদেশ বা ডিক্রি দ্বারা উক্ত সমিতিতে উক্ত সদস্যের শেয়ার বা অর্জিত ৬১[ মুনাফা] ক্রোকযোগ্য হইবে না বা উক্ত ডিক্রী বা আদেশ বলে শেয়ার বা ৬২[ মুনাফা] বাবদ প্রাপ্য সম্পদ হইতে আদায়যোগ্য হইবে না৷
 	 
 	 
